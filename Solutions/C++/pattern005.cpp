#include <iostream>
using namespace std;

int main(void)
{
    int i, j;
    for (i = 5; i >= 1; i--)
    {
        for (j = 5; j >= 1; j--)
        {
            cout << j;
        }
        cout << endl;
    }
    return 0;
}