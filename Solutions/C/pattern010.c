// Pattern Programs n.010 [C]
// Github Repository: https://github.com/mxsena/pattern-programs
#include <stdio.h>

int main(void)
{
    int i, j;
    for (i = i; i <= 5; i++)
    {
        for (j = 1; j <= 3; j++)
        {
            printf("%d %d", j, i);
        }
        printf("\n");
    }
    return 0;
}